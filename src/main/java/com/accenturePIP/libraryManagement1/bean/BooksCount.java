package com.accenturePIP.libraryManagement1.bean;

public class BooksCount {
	private String bookName;
	private String subjectName;
	private String authorName;
	private Long numbersOfCopies;
	public String getBookName() {
		return bookName;
	}
	public void setBookName(String bookName) {
		this.bookName = bookName;
	}
	public String getSubjectName() {
		return subjectName;
	}
	public void setSubjectName(String subjectName) {
		this.subjectName = subjectName;
	}
	public String getAuthorName() {
		return authorName;
	}
	public void setAuthorName(String authorName) {
		this.authorName = authorName;
	}
	public Long getNumbersOfCopies() {
		return numbersOfCopies;
	}
	public void setNumbersOfCopies(Long numbersOfCopies) {
		this.numbersOfCopies = numbersOfCopies;
	}
	

}
